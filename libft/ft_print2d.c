#include "libft.h"

int	ft_print2d(char **array)
{
	int i;
	int f;

	i = 0;
	f = 0;
	while (array[i])
	{
		f = 1;
		ft_putstr(array[i++]);
		ft_putchar('\n');
	}
	if (f == 0)
		return (-1);
	return (0);
}

